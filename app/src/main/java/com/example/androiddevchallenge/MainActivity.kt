@Composable
fun CountDownText(countDown: Int) {

    Text(
        text =
        countDown.toString(),
        fontSize = 150.sp
    )
}
// Start building your app here!
@ExperimentalAnimationApi
@Composable
fun MyApp(mainViewModel: MainViewModel = viewModel()) {

    val countDown: Int by mainViewModel.countDown.observeAsState(10)
    var countDownStart: Boolean by remember { mutableStateOf(false) }

    if (countDown == 0) {
        countDownStart = false
    }

    Surface(color = MaterialTheme.colors.background) {

        Column(
            modifier = Modifier
                .fillMaxWidth()
                .fillMaxHeight(),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {

            CountDownText(countDown)

            Button(
                onClick = {
                    countDownStart = true
                    mainViewModel.countUp()

                },
                enabled = !countDownStart
            ) {
                Text(
                    "Start",
                    fontSize = 40.sp
                )
            }
        }
    }
}

